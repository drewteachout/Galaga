void resetShip() {
	shippy.row = (240 - SHIP_WIDTH)/ 2 + 20;
	shippy.col = 130 - SHIP_HEIGHT;
	shippy.oldRow = shippy.row;
	shippy.oldCol = shippy.col;
}

void drawShip() {
	drawRect(shippy.oldRow, shippy.oldCol, SHIP_WIDTH, SHIP_HEIGHT, BLACK);
	drawImage3(shippy.row, shippy.col, SHIP_WIDTH, SHIP_HEIGHT, ship);
}

void resetBees() {
	for (int i = 0; i < numBees; i++) {
		bees[i].row = 12;
		bees[i].col = 200*(i + 1) / numBees;
		bees[i].alive = 1;
	}
}

void drawBees() {
	for (int i = 0; i < numBees; i++) {
		if (bees[i].alive) {
			//drawRect(bees[i].oldRow, bees[i].oldCol, BEE_WIDTH, BEE_HEIGHT, BLACK);
			drawImage3(bees[i].row, bees[i].col, BEE_WIDTH, BEE_HEIGHT, bee);
		}
		
	}
}

void resetDragonflys() {
	for (int i = 0; i < numDragonflys; i++) {
		dragonflys[i].row = 24;
		dragonflys[i].col = 20 + (i * 13);
		dragonflys[i].oldRow = dragonflys[i].row;
		dragonflys[i].oldCol = dragonflys[i].col;
		dragonflys[i].alive = 1;
	}
}

void drawDragonflys() {
	for (int i = 0; i < numDragonflys; i++) {
		if (dragonflys[i].alive) {
			drawRect(dragonflys[i].oldRow, dragonflys[i].oldCol, DRAGONFLY_WIDTH, DRAGONFLY_HEIGHT, BLACK);
			drawImage3(dragonflys[i].row, dragonflys[i].col, DRAGONFLY_WIDTH, DRAGONFLY_HEIGHT, dragonfly);
		}
	}
}

void addNewBullet() {
	for (int i = 0; i < numBullets; i++) {
		if (bullets[i].alive == 0) {
			bullets[i].row = shippy.row - (SHIP_HEIGHT / 2);
			bullets[i].col = shippy.col + (SHIP_WIDTH / 2 - 2);
			bullets[i].alive = 1;
			bullets[i].oldRow = bullets[i].row;
			bullets[i].oldCol = bullets[i].col;
			break;
		}
	}	
}

void addEnemyBullet(int n) {
	for (int i = 0; i < numEnemyBullets; i++) {
		if (enemyBullets[i].alive == 0) {
			enemyBullets[i].row = dragonflys[n].row + (DRAGONFLY_HEIGHT + 7);
			enemyBullets[i].col = dragonflys[n].col + (DRAGONFLY_WIDTH / 2 - 2);
			enemyBullets[i].alive = 1;
			enemyBullets[i].oldRow = enemyBullets[i].row;
			enemyBullets[i].oldCol = enemyBullets[i].col;
			break;
		}
	}
}

void addBeeBullet(int n) {
	for (int i = 0; i < numEnemyBullets; i++) {
		if (enemyBullets[i].alive == 0) {
			enemyBullets[i].row = bees[n].row + (BEE_HEIGHT + 7);
			enemyBullets[i].col = bees[n].col + (BEE_WIDTH / 2 - 2);
			enemyBullets[i].alive = 1;
			enemyBullets[i].oldRow = enemyBullets[i].row;
			enemyBullets[i].oldCol = enemyBullets[i].col;
			break;
		}
	}
}

void resetBullet() {
	for (int i = 0; i < numBullets; i++) {
		bullets[i].alive = 0;
	}
}

void resetEnemyBullet() {
	for (int i = 0; i < numEnemyBullets; i++) {
		enemyBullets[i].alive = 0;
	}
}

void drawBullet(int i) {
	if (bullets[i].alive) {
		setPixel(bullets[i].oldRow + BULLET_HEIGHT, bullets[i].col + 1, BLACK);
		drawImage3(bullets[i].row, bullets[i].col, BULLET_WIDTH, BULLET_HEIGHT, bullet);
	}
}

void drawEnemyBullet(int i) {
	if (enemyBullets[i].alive) {
		drawRect(enemyBullets[i].oldRow, enemyBullets[i].oldCol, ENEMYBULLET_WIDTH, ENEMYBULLET_HEIGHT, BLACK);
		drawImage3(enemyBullets[i].row, enemyBullets[i].col, ENEMYBULLET_WIDTH, ENEMYBULLET_HEIGHT, enemybullet);
	}
}

void moveBullets() {
	for (int i = 0; i < numBullets; i++) {
		if (bullets[i].alive) {
			bullets[i].oldRow = bullets[i].row;
			bullets[i].row--;
			if (bullets[i].row < -BULLET_HEIGHT - 1) {
				bullets[i].alive = 0;
			}
		}
	}
	for (int i = 0; i < numBullets; i++) {
		if (bullets[i].alive) {
			drawBullet(i);
		}
	}
}

void moveEnemyBullets() {
	for (int i = 0; i < numEnemyBullets; i++) {
		if (enemyBullets[i].alive) {
			enemyBullets[i].oldRow = enemyBullets[i].row;
			enemyBullets[i].row++;
			if (enemyBullets[i].row > 240) {
				enemyBullets[i].alive = 0;
			}
		}
	}
	for (int i = 0; i < numEnemyBullets; i++) {
		if (enemyBullets[i].alive) {
			drawEnemyBullet(i);
		}
	}
}

void moveDragonflys() {
	for (int i = 0; i < numDragonflys; i++) {
		if (dragonflys[i].alive) {
			if (dragonflys[i].row < 100) {
				dragonflys[i].oldRow = dragonflys[i].row;
				dragonflys[i].row++;
			} 
			if (dragonflys[i].col < 160 + (i * 13)) {
				dragonflys[i].oldCol = dragonflys[i].col;
				dragonflys[i].col++;
			}
			if (bulletCounter == 40 && roundCounter < 4) {
				enemyShoot(i);
			}
		}
	}
	if (bulletCounter == 50) {
		bulletCounter = 0;
		roundCounter++;
	}
	bulletCounter++;
	drawDragonflys();
}

void updateCollision() {
	for (int i = 0; i < numBullets; i++) {
		//Bullet with bee
		for (int j = 0; j < numBees; j++) {
			if (bees[j].alive && bullets[i].alive) {
				if (bullets[i].row < bees[j].row + BEE_HEIGHT && (bullets[i].col + 2 > bees[j].col && bullets[i].col < bees[j].col + BEE_WIDTH)) {
					drawRect(bees[j].row, bees[j].col, BEE_WIDTH, BEE_HEIGHT, BLACK);
					bees[j].alive = 0;
					drawRect(bullets[i].oldRow, bullets[i].oldCol, BULLET_WIDTH, BULLET_HEIGHT, BLACK);
					bullets[i].alive = 0;
				}
			}
		}
		// Bullet with dragonfly
		for (int j = 0; j < numDragonflys; j++) {
			if (dragonflys[j].alive && bullets[i].alive) {
				if ((bullets[i].row < dragonflys[j].row + DRAGONFLY_HEIGHT && bullets[i].row + BULLET_HEIGHT > dragonflys[j].row) && (bullets[i].col < dragonflys[j].col + DRAGONFLY_WIDTH && bullets[i].col + BULLET_WIDTH > dragonflys[j].col )) {
					drawRect(dragonflys[j].row, dragonflys[j].col, DRAGONFLY_WIDTH, DRAGONFLY_HEIGHT, BLACK);
					dragonflys[j].alive = 0;
					drawRect(bullets[i].oldRow, bullets[i].oldCol, BULLET_WIDTH, BULLET_HEIGHT, BLACK);
					bullets[i].alive = 0;
				}
			}	
		}
	}
	for (int i = 0; i < numEnemyBullets; i++) {
		if (enemyBullets[i].alive && lives) {
			if ((enemyBullets[i].row + ENEMYBULLET_HEIGHT > shippy.row && enemyBullets[i].row < shippy.row + SHIP_HEIGHT) && (enemyBullets[i].col < shippy.col + SHIP_WIDTH && enemyBullets[i].col + ENEMYBULLET_WIDTH > shippy.col)) {
				lives--;
				enemyBullets[i].alive = 0;
				drawRect(enemyBullets[i].oldRow, enemyBullets[i].oldCol, ENEMYBULLET_WIDTH, ENEMYBULLET_HEIGHT, BLACK);
				roundCounter = 0;
				break;
			}
		}
	}
}

int getLives() {
	return lives;
}

int enemiesDead() {
	int dead = 1;
	for (int i = 0; i < numBees; i++) {
		if (bees[i].alive) {
			dead = 0;
		}
	}
	for (int i = 0; i < numDragonflys; i++) {
		if (dragonflys[i].alive) {
			dead = 0;
		}
	}
	return dead;
}

void beeShoot() {
	for (int i = 0; i < numBees; i++) {
		if (bees[i].alive) {
			addBeeBullet(i);
		}
	}
}

